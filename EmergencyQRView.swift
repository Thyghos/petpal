            }
 
            VStack(alignment: .leading, spacing: 8) {
                InstructionRow(number: "1", text: "Save or print the QR code")
                InstructionRow(number: "2", text: "Attach to your pet's collar tag")
                InstructionRow(number: "3", text: "Anyone can scan to see emergency info")
                InstructionRow(number: "4", text: "They'll also discover Petpal!")
