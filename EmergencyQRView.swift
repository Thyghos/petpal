struct EmergencyQRView: View {
    @Environment(\.modelContext) private var modelContext
    @Environment(\.dismiss) private var dismiss
    @AppStorage("petName") private var petName: String = "Your Pet"
 
    @Query private var emergencyProfiles: [EmergencyProfile]
    @Query(sort: \Pet.dateAdded) private var pets: [Pet]
    @State private var showingEditor = false
    @State private var qrImageData: Data?
    #if os(iOS)
    @State private var sharePayload: ShareSheetPayload?
    #endif
    
    /// Emergency profile for the pet shown on Home / switched in-tab only.
    private var profilesForActivePet: [EmergencyProfile] {
        guard let pid = FeaturePetScope.resolvedPetId(pets: pets) else { return [] }
        return emergencyProfiles.filter { $0.linkedPetId == pid }
    }
 
    var currentProfile: EmergencyProfile? {
        profilesForActivePet.first { $0.isActive } ?? profilesForActivePet.first
    }
 
    var body: some View {
        NavigationStack {
            ZStack {
                LinearGradient(
                    colors: [Color("BrandCream"), Color("BrandSoftBlue").opacity(0.3)],
                    startPoint: .topLeading,
                    endPoint: .bottomTrailing
                )
                .ignoresSafeArea()
 
                VStack(alignment: .leading, spacing: 0) {
                    FeaturePetScopeHeader(pets: pets)
                    Text("Printable QR for lost pet with vital info")
                        .font(.subheadline)
                        .foregroundStyle(.secondary)
                        .frame(maxWidth: .infinity, alignment: .leading)
                        .padding(.horizontal, 20)
                        .padding(.top, 4)
                        .padding(.bottom, 8)
                    if let profile = currentProfile {
                        ScrollView {
                            VStack(spacing: 24) {
                                headerSection
                            qrCodeCard(profile: profile)
                            profileSummaryCard(profile: profile)
                            instructionsCard
                        }
                        .padding()
                    }
                    } else {
                        emptyStateView
                    }
                }
            }
            .navigationTitle("Emergency QR")
            .navigationBarTitleDisplayMode(.inline)
            .toolbar {
                ToolbarItem(placement: .cancellationAction) {
                    Button("Close") { dismiss() }
                }
                ToolbarItem(placement: .primaryAction) {
                    HStack(spacing: 12) {
                        #if os(iOS)
                        if let profile = currentProfile {
                            Menu {
                                Button {
                                    let printable = EmergencyQRPrintableView(profile: profile)
                                    if let img = PrintShareHelper.renderToImage(printable) {
                                        let text = "Emergency QR for \(profile.petName) — Generated by Petpal"
                                        DispatchQueue.main.async {
                                            sharePayload = ShareSheetPayload(items: [img, text])
                                        }
                                    }
                                } label: {
                                    Label("Share", systemImage: "square.and.arrow.up")
                                }
                                Button {
                                    let printable = EmergencyQRPrintableView(profile: profile)
                                    DispatchQueue.main.async {
                                        PrintShareHelper.printView(printable, title: "Emergency QR")
                                    }
                                } label: {
                                    Label("Print", systemImage: "printer")
                                }
                            } label: {
                                Image(systemName: "ellipsis.circle")
                            }
                        }
                        #endif
                        Button {
                            showingEditor = true
                        } label: {
                            Image(systemName: currentProfile == nil ? "plus.circle.fill" : "pencil.circle.fill")
                                .font(.title2)
                                .foregroundStyle(Color("BrandOrange"))
                        }
                    }
                }
            }
            .sheet(isPresented: $showingEditor) {
                EmergencyProfileEditor(profile: currentProfile)
            }
            #if os(iOS)
            .sheet(item: $sharePayload) { payload in
                ShareSheet(items: payload.items)
            }
            #endif
            .onAppear {
                migrateLegacyEmergencyProfilesIfNeeded()
            }
        }
    }
    
    /// Split legacy global profiles into one copy per pet (same pattern as Pet Care Notes).
    private func migrateLegacyEmergencyProfilesIfNeeded() {
        let orphans = emergencyProfiles.filter { $0.linkedPetId == nil }
        var petsWithProfile = Set(emergencyProfiles.compactMap { $0.linkedPetId })
        
        if let template = orphans.first, !pets.isEmpty {
            for pet in pets where !petsWithProfile.contains(pet.id) {
                modelContext.insert(Self.duplicateEmergencyProfile(template, linkedPetId: pet.id))
                petsWithProfile.insert(pet.id)
            }
            for o in orphans {
                modelContext.delete(o)
            }
            try? modelContext.save()
            return
        }
        
        if let pid = FeaturePetScope.resolvedPetId(pets: pets), !emergencyProfiles.contains(where: { $0.linkedPetId == pid }) {
            let new = EmergencyProfile(linkedPetId: pid, petName: petName)
            modelContext.insert(new)
            try? modelContext.save()
        }
    }
    
    private static func duplicateEmergencyProfile(_ template: EmergencyProfile, linkedPetId: UUID) -> EmergencyProfile {
        EmergencyProfile(
            linkedPetId: linkedPetId,
            petName: template.petName,
            ownerName: template.ownerName,
            ownerPhone: template.ownerPhone,
            ownerEmail: template.ownerEmail,
            alternateContact: template.alternateContact,
            medications: template.medications,
            allergies: template.allergies,
            medicalConditions: template.medicalConditions,
            microchipNumber: template.microchipNumber,
            vetName: template.vetName,
            vetPhone: template.vetPhone,
            vetAddress: template.vetAddress,
            feedingInstructions: template.feedingInstructions,
            specialNeeds: template.specialNeeds,
            lostPetMessage: template.lostPetMessage,
            rewardOffered: template.rewardOffered,
            isActive: template.isActive,
            lastUpdated: template.lastUpdated
        )
    }
 
    private var headerSection: some View {
        VStack(spacing: 8) {
            Image(systemName: "qrcode")
                .font(.system(size: 50))
                .foregroundStyle(Color("BrandOrange"))
            Text("Emergency Collar Tag")
                .font(.title2)
                .fontWeight(.bold)
