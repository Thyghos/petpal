            if let data = generateQRCode(from: profile.emergencyURL) {
                qrCodeImage(from: data)
                    .padding()
                    .background(Color.white)
                    .cornerRadius(16)
                    .shadow(color: Color.black.opacity(0.1), radius: 8)
                    .onAppear { qrImageData = data }
            }
 
            Text(profile.petName)
                .font(.title3)
                .fontWeight(.bold)
                .foregroundStyle(Color("BrandOrange"))
 
            HStack(spacing: 16) {
                Button {
                    #if os(iOS)
                    if let data = qrImageData, let image = UIImage(data: data) {
                        let text = "Emergency contact for \(profile.petName) — Generated by Petpal"
                        DispatchQueue.main.async {
                            sharePayload = ShareSheetPayload(items: [image, text])
                        }
                    }
                    #endif
                } label: {
                    Label("Share", systemImage: "square.and.arrow.up")
                        .font(.headline)
                        .foregroundStyle(.white)
                        .padding(.horizontal, 24)
                        .padding(.vertical, 12)
                        .background(Color("BrandOrange"))
                        .clipShape(Capsule())
                }
 
                Button {
                    #if os(iOS)
                    if let data = qrImageData, let image = UIImage(data: data) {
                        UIImageWriteToSavedPhotosAlbum(image, nil, nil, nil)
                    }
                    #endif
                } label: {
                    Label("Save", systemImage: "square.and.arrow.down")
                        .font(.headline)
                        .foregroundStyle(Color("BrandOrange"))
                        .padding(.horizontal, 24)
                        .padding(.vertical, 12)
                        .background(Color("BrandOrange").opacity(0.15))
                        .clipShape(Capsule())
                }
            }
        }
        .padding()
        .background(qrCardBackgroundColor)
        .clipShape(RoundedRectangle(cornerRadius: 20))
        .shadow(color: Color.black.opacity(0.07), radius: 8, x: 0, y: 4)
    }
 
    private var qrCardBackgroundColor: Color {
        #if os(iOS)
        return Color(.systemBackground)
        #elseif os(macOS)
        return Color(nsColor: .windowBackgroundColor)
        #else
        return Color.white
        #endif
    }
 
